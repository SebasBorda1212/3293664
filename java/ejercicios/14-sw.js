let nota = "B";

switch (nota) {
  case "A":
    console.log("Excelente");
    break;
  case "B":
    console.log("Bien");
    break;
  case "C":
    console.log("Regular");
    break;
  case "D":
   console.log("Deficiente");
    break;
  case "F":
    console.log("Reprobado");
    break;
  default:
    console.log("Calificación inválida");
}



