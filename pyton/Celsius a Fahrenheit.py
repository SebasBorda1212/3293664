# de Celsius a Fahrenheit


print("comvierte la temperatura de Celsius a Fahrenheit")

def Celsius_a_Fahrenheit():

    Celsius = float(input("ingresela tempoeratura en grados celsius: "))
    Fahrenheit = (1.8* Celsius)+32

    print("la temperatura en fahrenheit es de: ", Fahrenheit)
Celsius_a_Fahrenheit()